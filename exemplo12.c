// Bibliotecas
#include <stdio.h> 

// Programa Principal
int main()
{
	// variáveis
	int a, b, c;
	
	a = 3; b = 4; c = 5;
	
	// verificar se é par ou ímpar
	if (a % 2 == 0)
	{
		printf("O numero %d e par", a);
	} else {
		printf("O numero %d e impar", a);
	}
	
	if (b % 2 == 0)
	{
		printf("O numero %d e par", b);
	} else {
		printf("O numero %d e impar", b);
	}
	
	if (c % 2 == 0)
	{
		printf("O numero %d e par", c);
	} else {
		printf("O numero %d e impar", c);
	}
	
}








